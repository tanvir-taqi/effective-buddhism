// search text in paragraphs


const searchBox = document.getElementById('search-box');
const searchBtn = document.getElementById('search-btn');

searchBtn.addEventListener('click', () => {

    const searchText = searchBox.value.toLowerCase();

    const paragraphs = document.getElementsByTagName('p');

    let firstMatch = true;
    
    for (let i = 0; i < paragraphs.length; i++) {
        const paragraph = paragraphs[i];
        const paragraphText = paragraph.textContent.toLowerCase();

        if (paragraphText.includes(searchText)) {
 
            if (firstMatch) {
                paragraph.scrollIntoView({ behavior: 'smooth' });
                firstMatch = false;
            }

            const highlightedText = paragraphText.replace(new RegExp(searchText, 'gi'), `<span class="highlight">${searchText}</span>`);
            paragraph.innerHTML = highlightedText;          
        }
    }
});


